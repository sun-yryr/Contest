#define ulong unsigned long

const int MOD = 1e9 + 7;

const int MAX = 2001;

long long fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
long long COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

int main() {
    int n, k;
    COMinit();
    cin >> n >> k;
    for(int i=1; i<=k; i++) {
        ll left = COM(n-k+1, i);
        ll right = COM(k-1, i-1);
        printf("%lld\n", (left*right)%MOD);
    }
    return 0;
}

